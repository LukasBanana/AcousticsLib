#ifndef AC_MUSICAL_NOTES_H
#define AC_MUSICAL_NOTES_H


namespace Ac
{


//! Musical notes scale.
enum class MusicalNotes
{
    C       =  0, //!< C (EN), C (DE)
    CSharp  =  1, //!< C# (EN), Cis (DE)
    Db      =  1, //!< Db (EN), Des (DE)
    D       =  2, //!< D (EN), D (DE)
    DSharp  =  3, //!< D# (EN), Dis (DE)
    Eb      =  3, //!< Eb (EN), Es (DE)
    E       =  4, //!< E (EN), E (DE)
    F       =  5, //!< F (EN), F (DE)
    FSharp  =  6, //!< F# (EN), Fis (DE)
    Gb      =  6, //!< Gb (EN), Ges (DE)
    G       =  7, //!< G (EN), G (DE)
    GSharp  =  8, //!< G# (EN), Gis (DE)
    Ab      =  8, //!< Ab (EN), As (DE)
    A       =  9, //!< A (EN), A (DE)
    ASharp  = 10, //!< A# (EN), Ais (DE)
    Bb      = 10, //!< Bb (EN), B (DE)
    B       = 11, //!< B (EN), H (DE)
};


} // /namespace Ac


#endif



